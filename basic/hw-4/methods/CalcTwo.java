package methods;

public class CalcTwo {
    public static float twoSum(float fnum, float snum) {
        return fnum + snum;
    }

    public static float twoSub(float fnum, float snum) {
        return fnum - snum;
    }

    public static float twoMul(float fnum, float snum) {
        return fnum * snum;
    }

    public static float twoDiv(float fnum, float snum) {
        return fnum / snum;
    }
}
